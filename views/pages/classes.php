            <h2 class="classesTitle text-center">Les classes de Maternelle</h2>
            <p>L'école maternelle accueille les enfants dans un espace adapté , très gai, coloré constitué de 5 classes, 
            de deux dortoirs, de deux espaces sanitaires récents, d’une salle de motricité. La cour maternelle, à l’abri, 
            est fermée sur trois côtés et permet aux enfants de jouer en toute tranquillité.</p> 
            
            <p>L’équipe pédagogique porte un 
            projet basé sur le développement de l’enfant selon son  rythme et ses besoins, en adéquation avec les valeurs 
            du projet d’établissement. En particulier, à l’école maternelle les enfants doivent s’épanouir, tout en apprenant 
            à se respecter, respecter les autres et à partager solidairement l’attention des adultes. Ils acquièrent le langage 
            et le lexique fondamentaux pour développer leur capacité à s’exprimer et à  repérer dans l’espace et le temps par le 
            biais de tous les projets : pédagogie du jeu, motricité quotidienne, intervenants en musique, théâtre, etc.</p>

            <p>Les agents de service maternel (ASEM) accompagnent les enfants tout au long de la journée de 7h15 le matin jusqu’à 
            19 heures le soir pour ceux qui restent en garderie, et assurent le relais avec les familles. Notre maternelle assure 
            en douceur la transition pour donner aux enfants toutes les chances de réussir en primaire. Un travail en équipe permet
            aux enseignants de suivre l’évolution de chaque enfant et d’individualiser son parcours.</p>

            <p>Les enfants âgés de moins de trois ans sont accueillis de façon progressive sur des matinées à partir de deux ans 
            et demi, dans les classes de PS. Ils sont pris en charge et des activités adaptées leur sont proposées. Ils prennent 
            le temps de découvrir l’école et de s’intégrer en douceur, pour continuer ensuite régulièrement à la journée lorsqu’ils 
            ont trois ans révolus.</p>
