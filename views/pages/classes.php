            <h2 class="classesTitle text-center" id="classesPrimaire">Les classes du Primaire</h2>
            <p>L'école primaire constituée de 8 classes propose un projet pour conduire tous les enfants vers la maitrise des savoirs 
            fondamentaux mais aussi vers l’acquisition de savoir-faire et de savoirs être. Le cycle 2 est constitué du CP, CE1 et CE2. 
            Le cycle 3 regroupe le CM1 et le CM2 en lien avec la 6ème.</p>

            <p>Nous privilégions la prise en charge des élèves en CP et en CE1, par des classes avec des effectifs moins importants et 
            un décloisonnement avec des enseignantes de maternelle qui viennent co animer des ateliers en début d’après-midi.</p>

            <p>Les apprentissages très tournés vers la maitrise du langage écrit en cycle 2 évoluent pour permettre aux élèves de s’ouvrir 
            vers tous les domaines littéraires, scientifiques, artistiques, culturels et sportifs. Nous proposons à partir de la grande 
            section, mais surtout en primaire des projets sportifs, culturels et linguistiques en CM2 (Jersey) sous la forme de classes de 
            découvertes ou de sorties pédagogiques régulières en lien avec nos projets de classe.</p>

            <p>Nous adaptons les apprentissages selon les besoins des élèves. Chaque élève bénéficie du regard de l’équipe éducative qui 
            travaille dans la continuité de ce qui a été mis en place pour l’élève, y compris entre la maternelle et le primaire. Nous 
            sollicitons l’éclairage d’une enseignante spécialisée qui intervient sur des petits groupes ou en co-animation en classe quand 
            des difficultés particulières sont détectées.</p>

            <p>Nous évaluons les compétences telles qu’elles sont demandées dans les programmes de l’éducation nationale mais nous attachons 
            aussi au développement de la personnalité de l’élève, en l’aidant à mieux se connaitre. L’élève est une personne en devenir.</p>

            <p>La relation avec les familles est importante et nous demandons aux parents tels que le définit le règlement intérieur une bonne 
            collaboration et une adhésion aux valeurs de l’établissement, en particulier, le respect des horaires d’enseignement, de la présence 
            régulière et obligatoire en classe et le suivi du travail de l’élève.</p>
