        <h2 class="reglementTitle col-md-12 mb-3">Pour les élèves</h2>
        <p>Apprendre à s’engager par rapport aux valeurs de notre école avec :</p>
        <ul class="listeReglement">
            <li>Le respect des règles nécessaires à une vie communautaire</li>
            <li>Une réflexion sur ses actes et les sanctions qui s’y rapportent</li>
            <li>Le respect des autres enfants et de tous les adultes qui interviennent dans l’établissement</li>
            <li>Le respect du cadre de vie</li>
            <li>L’engagement dans le travail scolaire</li>
            <li>La participation active à l’ensemble des projets</li>
            <li>L’investissement dans la vie de l’école</li>
            <li>La coopération et la fraternité</li>
        </ul>

        <h2 class="reglementTitle col-md-12 mb-3">Pour les parents</h2> 
        <p>L’acceptation pleine et entière du projet éducatif par les familles qui ont librement choisi cette école et son projet.
            Cet engagement se manifeste par :</p>
        <ul class="listeReglement">
            <li>Le respect du fonctionnement de l’école, des horaires, des règles de sécurité, du rôle des adultes</li>
            <li>La garantie de la présence de votre enfant sur le temps scolaire</li>
            <li>L’information au chef d’établissement en cas d’absence pour maladie. En cas d’absence prévisible, informer préalablement le chef d’établissement de l’école en précisant le motif (s’il y a doute sur la légitimité du motif, le chef d’établissement invite les personnes responsables de l’enfant à présenter une demande d’autorisation d’absence qu’il transmet à l’inspecteur d’académie, directeur des services départementaux de l’éducation nationale)</li>
            <li>Le respect du travail des enseignants et du personnel de la communauté éducative</li>
            <li>Un intérêt porté au travail scolaire de l’enfant par un suivi et une participation aux réunions de classe proposées</li>
            <li>Une communication constructive basée sur une confiance mutuelle</li>
            <li>Une participation à la vie de l’école en fonction de ses possibilités, certaines activités ne pouvant se faire sans votre aide</li>
        </ul>

        <h2 class="reglementTitle col-md-12 mb-3">Pour l'équipe éducative</h2> 
        <ul class="listeReglement">
            <li>Accueillir toutes les familles dans le respect des convictions de chacun</li>
            <li>Garantir le respect des programmes officiels</li>
            <li>Promouvoir la solidarité au sein de l’équipe éducative</li>
            <li>Créer un climat de confiance et de bienveillance avec les enfants et leur famille</li>
        </ul>

        <h2 class="reglementTitle col-md-12 mb-3">Favoriser</h2> 
        <p>L’intégration des enfants à un besoin particulier facilitée par :</p>
        <ul class="listeReglement">
            <li>La curiosité et l’enrichissement par la proposition d’activités culturelles, artistiques et sur le patrimoine.</li>
            <li>Une ouverture culturelle européenne.</li>
            <li>La responsabilité, l’autonomie, l’écoute et l’éducation à la citoyenneté par des temps de parole</li>
            
        </ul>
